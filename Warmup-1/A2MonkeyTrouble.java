package WarmUp1;
public class A2MonkeyTrouble {
    public static boolean monkeyTrouble(boolean aSmile, boolean bSmile){
        if((aSmile && bSmile) || (!aSmile && !bSmile)){
            return true;
        }
        return false;
    }
}
